			ENTENDENDO O CAFÉ ESPECIAL<br><br>
			O termo “café especial” foi cunhado pela primeira vez em 1974 pela especialista norueguesa Erna Knutsen em uma edição do “Tea & Coffee Trade Journal”. Na época, Knutsen, utilizou o termo para designar cafés de altíssima qualidade produzidos em “micro-climas”.<br><br>

			Para ser considerado um café especial, o grão precisa passar por diversos critérios de qualidade. No Brasil, a responsável por certificar estes grãos é a BSCA (Brazilian Specialty Coffee Association) seguindo os critérios da SCA (Specialty Coffee Association) que julga desde características físicas, como origens, variedades, cor e tamanho, ausência de defeitos, até preocupações de ordem ambiental e social, como os sistemas de produção e as condições de trabalho da mão-de-obra cafeeira.<br><br>

			Hoje, sua definição mais amplamente aceita é para os cafés que são compostos somente de grãos arábica, livre de defeitos capitais (grãos pretos, verdes e ardidos) e que atingem uma nota mínima de 80 pontos dentro de 100 pontos possíveis quando avaliados por um especialista, o Q-Grader.<br><br><br>


			A IMPORTÂNCIA DA TORRA<br><br>
			Uma vez escolhidos e selecionados a dedo, os grãos dos cafés especiais chegam ao Suplicy ainda verdes (crus) para passar pelo processo que os transformarão na bebida aromática e saborosa que você conhece: a torra.<br><br>

			É quase que uma metamorfose, na qual eles mudam de cor, ficam mais leves, perdem água, aumentam de volume e adquirem mais aroma. Isso porque, é exatamente a exposição a altas temperaturas que permite aos grãos desenvolverem suas qualidades aromáticas, sabor, corpo, acidez, finalização e equilíbrio. O mesmo grão de café pode ter características potencializadas ou depreciadas conforme o perfil de torra utilizado.<br><br>

			Recebemos amostras de várias regiões e produtores do Brasil. A primeira parte desse processo é descobrir o potencial de cada café fazendo um cupping.<br><br>

			O café especial oferece uma gama de possibilidades e, apesar de complexo, nossos especialistas avaliam sua vocação olhando primeiramente para as características principais do café, como doçura e acidez, além dos aromas e sabores e como tudo isso se harmoniza na bebida. Após isso definimos a compra do grão verde e a curva de torra a ser aplicada.<br><br>

			Nessa hora, entram em cena a temperatura e o tempo de exposição ao calor, determinantes para realçar as características de sabor, aroma, corpo e acidez de cada tipo de grão. É nessa hora que o café de qualidade mostra seu verdadeiro valor; e é no processo de torra em que alguns dos defeitos do café são evidenciados quando presentes e produzem uma bebida de sabor nada agradável.<br><br>

			Fazemos novamente o cupping, para verificar se a bebida corresponde com o que foi apresentado antes da compra e caso necessite vamos fazendo uma sintonia fina na curva de torra para chegar no resultado esperado.<br><br>
